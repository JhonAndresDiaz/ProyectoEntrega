    private void btnBuscarNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarNombreActionPerformed
        if(txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre del medico que desea buscar");
        }else {
            String datoNombre = txtNombre.getText();        
            Consulta validarMedico = controladorHistorial.buscarConsultaMedico(datoNombre);
            if(validarMedico != null){
                String[] titulos = new String[] {"Estado", "Codigo", "Medico", "Motivo", "Valoracion", "Notas", "Diagnostico"};
                modelo.setColumnIdentifiers(titulos);
                tablaHistorial.setModel(modelo); 
                limpiarCampos();
                actualizarTablaMedico(datoNombre);
            }else {
                JOptionPane.showMessageDialog(null, "El medico buscado no cuenta con colsutas atendidas en su historial");
            }
        }
    }//GEN-LAST:event_btnBuscarNombreActionPerformed

    private void btnBuscarDiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarDiaActionPerformed
        if(dateFiltroDia.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Ingrese el numero del dia que desea buscar en su historial");
        }else {
            Date fecha = dateFiltroDia.getDate();
            LocalDate fechaDia = new java.sql.Date(fecha.getTime()).toLocalDate();
            Consulta validarFecha = controladorHistorial.buscarConsultaFecha(fechaDia);
            if(validarFecha != null) {
                String [] titulos = new String[] {"Estado", "Codigo", "Fecha", "Motivo", "Valoracion", "Notas", "Diagnostico"};
                modelo.setColumnIdentifiers(titulos);
                tablaHistorial.setModel(modelo);
                limpiarCampos();
                actualizarTablaFecha(fechaDia);
            }else {
                JOptionPane.showMessageDialog(null, "El dia buscado no cuenta con consultas atendidas");
            }
        }
    }//GEN-LAST:event_btnBuscarDiaActionPerformed

    private void btnConsultasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultasActionPerformed
        String [] titulos = new String[] {"Fecha", "Codigo", "Fecha", "Motivo", "Valoracion", "Notas", "Diagnostico"};
        modelo.setColumnIdentifiers(titulos);
        tablaHistorial.setModel(modelo);
        actualizarTablaConsultas();
    }//GEN-LAST:event_btnConsultasActionPerformed

    public void limpiarCampos(){
        dateFiltroDia.setDate(null);
        txtNombre.setText(null);
    }
    
    
    public void actualizarTablaFecha(LocalDate datoFecha){
        
        ArrayList<Consulta> consultas = controladorHistorial.conseguirConsultasFecha(paciente, datoFecha);
        
        try{
            for (int i = 0; i < 3 ; i++) {
                for (int j = 0; j < modelo.getRowCount(); j++) {
                    modelo.removeRow(j);
                }
            }
        }catch(NullPointerException e){
        }
            try{
                for (int i = 0; i < consultas.size() ; i++) {                    
                      Consulta aux = consultas.get(i);
                      Object[] ob = {aux.getEstado() , aux.getCodigo(), aux.getFechaAtencion(), aux.getMotivo(), aux.getValoracion(), aux.getNotas(), aux.getDiagnostico()};
                      modelo.addRow(ob);                     
                }
            }catch(NullPointerException e){        
        } 
    }

    public void actualizarTablaMedico(String nombre){
        
        ArrayList<Consulta> consultas = controladorHistorial.conseguirConsultasMedicoN(nombre, paciente);
        
        try{
            for (int i = 0; i < 3 ; i++) {
                for (int j = 0; j < modelo.getRowCount(); j++) {
                    modelo.removeRow(j);
                }
            }
        }catch(NullPointerException e){
        }
            try{
                for (int i = 0; i < consultas.size() ; i++) {
                      Consulta aux = consultas.get(i);
                      Object[] ob = {aux.getEstado(), aux.getCodigo(), aux.getMedico().getNombres() + " " + aux.getMedico().getApellidos(), aux.getMotivo(), aux.getValoracion(), aux.getNotas(),aux.getDiagnostico()};
                      modelo.addRow(ob);  
                }                   
            }catch(NullPointerException e){        
        } 
    }
    
