    public void limpiarCampos(){
        txtMotivoPqrs.setText(null);
        txtDetallepqrs.setText(null);
    }
    
    public void bloquear1(){
        
        ArrayList<Peticion> contador = controladorGestionPQRS.validarMaximoPeticiones(paciente);             
        if(contador.size() >= 3) {
            btnCrearPQRS.setEnabled(false);
            txtDetallepqrs.setEditable(false);
            txtMotivoPqrs.setEditable(false);
            JOptionPane.showMessageDialog(null, "Ha cumplido el máximo de 3 peticiones activas o en progreso");
        }   
    }
    
    public void actualizarTablaFecha(){
             
        ArrayList<Consulta> consultas = controladorGestionPQRS.conseguirConsultas(paciente);
        
        try{
            for (int i = 0; i < 3 ; i++) {
                for (int j = 0; j < modelo.getRowCount(); j++) {
                    modelo.removeRow(j);
                }
            }
        }catch(NullPointerException e){
        }
            try{
                for (int i = 0; i < consultas.size() ; i++) {                    
                    Consulta aux = consultas.get(i);
                    Object[] ob = {aux.getCodigo() , aux.getFechaAtencion(), aux.getMedico().getNombres() + " " + aux.getMedico().getApellidos(), aux.getMedico().getIdentificacion(), aux.getMotivo(), aux.getDiagnostico(), aux.getNotas()};
                    modelo.addRow(ob);              
                }
            }catch(NullPointerException e){        
        } 
    }
    
    
    
