    private void btnCrearPQRSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearPQRSActionPerformed
        if(txtDetallepqrs.getText().isEmpty() || txtMotivoPqrs.getText().isEmpty() || tablaConsulta.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Complete toda la información");
        }else {
            Date hoy = new Date();
            LocalDate fechaActual = new  java.sql.Date(hoy.getTime()).toLocalDate();
            int fila = tablaConsulta.getSelectedRow();        
            String motivo = txtMotivoPqrs.getText();
            String detalle = txtDetallepqrs.getText(); 
            String estado = "Activo";
            String codigo =  tablaConsulta.getValueAt(fila, 0).toString();
            Consulta consulta = controladorGestionPQRS.buscarCodigo(codigo);
            Peticion peticion = new Peticion(consulta, consulta.getCodigo(), motivo, fechaActual, detalle, estado, "", "");
            
            try{
                controladorGestionPQRS.agendarPeticion(peticion);
                JOptionPane.showMessageDialog(null, "Se ha creado exitosamente la PQRS con codigo numero: "+codigo);
                actualizarTablaFecha();
                bloquear1();
                limpiarCampos(); 
            }catch(PeticionCodigoExisteException | ConsultaCodigoYaExisteException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
                limpiarCampos();
            }
        }  
    }//GEN-LAST:event_btnCrearPQRSActionPerformed

