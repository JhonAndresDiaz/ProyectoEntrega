    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if(txtIdBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el numero de identificacion que desea buscar");
            return;
        }

        String identificacion = txtIdBuscar.getText();
        Enfermera enfermera = (Enfermera) controladorEnfermera.buscarEnfermeraId(identificacion);

        if (enfermera != null) {
            txtNombres.setText(enfermera.getNombres());
            txtApellidos.setText(enfermera.getApellidos());
            txtEdad.setText(String.valueOf(enfermera.getEdad()));
            txtIdentificacion.setText(enfermera.getIdentificacion());
            cboGenero.setSelectedItem(enfermera.getGenero());
            txtAniosExperiencia.setText(String.valueOf(enfermera.getAniosExperiencia()));
            txtTelefono.setText(enfermera.getNumTelefono());
            txtCorreo.setText(enfermera.getCorreo());
            txtTarjetaProfesional.setText(enfermera.getTarjetaProfesional());
            txtContrasenia.setText(enfermera.getContrasenia());
            txtConfirmarContrasenia.setText(enfermera.getContrasenia());
        } else {
            JOptionPane.showMessageDialog(null, "La Enfermera con numero de identificación: " + identificacion + " no se encuentra registrado");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

