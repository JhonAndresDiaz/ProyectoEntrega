    private void actualizarTabla(){
        ArrayList<Enfermera> enfermeras = controladorEnfermera.obtenerEnfermeras();
        try{
            for (int i = 0; i < 3 ; i++) {
                for (int j = 0; j < modelo2.getRowCount(); j++) {
                    modelo2.removeRow(j);
                }
            }
        }catch(NullPointerException e){
        }
            try{
                for (int i = 0; i < enfermeras.size() ; i++) {
                    Enfermera aux = enfermeras.get(i);                   
                    Object[] ob = {aux.getNombres(), aux.getIdentificacion(), aux.getCorreo(), aux.getContrasenia()};
                    modelo2.addRow(ob);
                }
            }catch(NullPointerException e){        
            }
    }
    
