    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        if(txtNombres.getText().isEmpty() || txtApellidos.getText().isEmpty() || txtIdentificacion.getText().isEmpty() || txtAniosExperiencia.getText().isEmpty() || txtEdad.getText().isEmpty() || txtTarjetaProfesional.getText().isEmpty() ||txtTelefono.getText().isEmpty() || cboGenero.getSelectedIndex() == 0 || txtContrasenia.getText().isEmpty() || txtCorreo.getText().isEmpty() || txtConfirmarContrasenia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Existen campos obligatorios por completar");
            return;
        }

        if(!txtContrasenia.getText().equals(txtConfirmarContrasenia.getText())){
            JOptionPane.showMessageDialog( null , " las contraseñas no coinciden");
            return;
        }

        String nombres = txtNombres.getText();
        String apellidos = txtApellidos.getText();
        int edad = Integer.parseInt(txtEdad.getText());
        String identificacion = txtIdentificacion.getText();
        String genero = String.valueOf(cboGenero.getSelectedItem());
        int aniosExperiencia = Integer.parseInt(txtAniosExperiencia.getText());
        String telefono = txtTelefono.getText();
        String tarjetaProfesional = txtTarjetaProfesional.getText();
        String correo = txtCorreo.getText();
        String contrasenia = txtContrasenia.getText();
        String rol = "Enfermera";

        Enfermera persona = new Enfermera(tarjetaProfesional, aniosExperiencia, identificacion, nombres, apellidos, edad, genero, telefono, correo, contrasenia, rol);

        try{
            controladorEnfermera.guardarEnfermera(persona);
            JOptionPane.showMessageDialog(null, "La enfermera/o " +nombres +" "+apellidos +  " ha sido agregado");
            limpiarCampos();
            actualizarTabla();
        }catch(RegistradoException  | CorreoRegistradoException | InformacionEnfermeraPacienteException ex ){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarseActionPerformed

