    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if(txtIdBuscar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el numero de identificacion que desea buscar");
            return;
        }

        String identificacion = txtIdBuscar.getText();
        Medico medico = (Medico) controladorMedico.buscarMedicoIdentificacion(identificacion);

        if (medico != null) {
            txtNombres.setText(medico.getNombres());
            txtApellidos.setText(medico.getApellidos());
            txtEdad.setText(String.valueOf(medico.getEdad()));
            txtIdentificacion.setText(medico.getIdentificacion());
            cboGenero.setSelectedItem(medico.getGenero());
            cboEspecialidades.setSelectedItem(medico.getEspecializacion());
            cboInicioHora.setSelectedItem(String.valueOf(medico.getHoraInicio().getHour()));
            cboInicioMin.setSelectedItem(String.valueOf(medico.getHoraInicio().getMinute()));
            cboFinHora.setSelectedItem(String.valueOf(medico.getHoraFinal().getHour()));
            cboFinMin.setSelectedItem(String.valueOf(medico.getHoraFinal().getMinute()));
            txtTelefono.setText(medico.getNumTelefono());
            txtCorreo.setText(medico.getCorreo());
            txtContrasenia1.setText(medico.getContrasenia());
            txtConfirmarContrasenia.setText(medico.getContrasenia());
        } else {
            JOptionPane.showMessageDialog(null, "El Medico con numero de identificación: " + identificacion + " no se encuentra registrado");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

