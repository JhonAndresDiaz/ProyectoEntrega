    private void actualizarComboBox (){
        cboEspecialidades.removeAllItems();
        ArrayList<String> especialidades = controladorMedico.getEspecialidades();
        for (int i = 0; i < especialidades.size(); i++) {
            if(especialidades.get(i) != null){
                cboEspecialidades.addItem(especialidades.get(i));
            }
        }
        cboEspecialidades.setSelectedIndex(0);
    }
    
    private void actualizarTabla(){
        ArrayList<Medico> medicos = controladorMedico.obtenerMedicos();
        try{
            for (int i = 0; i < 3 ; i++) {
                for (int j = 0; j < modelo1.getRowCount(); j++) {
                    modelo1.removeRow(j);
                }
            }
        }catch(NullPointerException e){
        }
            try{
                for (int i = 0; i < medicos.size() ; i++) {
                    Medico aux = medicos.get(i);                   
                    Object[] ob = {aux.getNombres(), aux.getIdentificacion(), aux.getCorreo(), aux.getContrasenia()};
                    modelo1.addRow(ob);
                }
            }catch(NullPointerException e){        
            }
    }
    
