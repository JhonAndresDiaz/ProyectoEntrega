    public void actualizarEspecialidades(){
        cboEspecialidades.removeAllItems();
        ArrayList<String> especialidades = controladorMedicoFiltro.getEspecialidades();
        for (int i = 0; i < especialidades.size(); i++) {
            if(especialidades.get(i) != null){
                if(!especialidades.get(i).equals("Añadir Especialidad Nueva") && !especialidades.get(i).equals("-Elegir-")){
                    cboEspecialidades.addItem(especialidades.get(i));
                }
            }
        }
        cboEspecialidades.setSelectedIndex(-1);
    }

    public void actualizarTablaAtenciones(String identificacion){
        
        Medico medico = (Medico) controladorMedicoFiltro.buscarMedicoIdentificacion(identificacion);
        ArrayList<Consulta> consultas = controladorMedicoFiltro.conseguirConsultas(medico);
        
        try{
            for (int i = 0; i < 3 ; i++) {
                for (int j = 0; j < modelo.getRowCount(); j++) {
                    modelo.removeRow(j);
                }
            }
        }catch(NullPointerException e){
        }
            try{
                for (int i = 0; i < consultas.size() ; i++) {                    
                      Consulta aux = consultas.get(i);
                      Object[] ob = {aux.getFechaAtencion(), aux.getPaciente().getNombres() + " " +aux.getPaciente().getApellidos(), aux.getMotivo(), aux.getDiagnostico(), aux.getValoracion() };
                      modelo.addRow(ob);                     
                }
            }catch(NullPointerException e){        
        } 
    }
    
