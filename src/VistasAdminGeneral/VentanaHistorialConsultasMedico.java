    private void btnMedicosConsultasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMedicosConsultasActionPerformed
        if(dateFechaInicial.getDate() == null || dateFechaFinal.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Seleccione el rango que desea buscar");
        }else {
            Date xInicio = dateFechaInicial.getDate();
            Date xFinal = dateFechaFinal.getDate();
            LocalDate fechaInicio = new java.sql.Date(xInicio.getTime()).toLocalDate();
            LocalDate fechaFin = new java.sql.Date(xFinal.getTime()).toLocalDate();
            ArrayList<Consulta> consultas = Singleton.PersonaSingleton.getInstancia().getConsultas();
            ConsultaInfo consultaInfo = obtenerMedicoMasConsultas(consultas, fechaInicio, fechaFin);
         
            if (consultaInfo != null) {
            JOptionPane.showMessageDialog(null, "Médico con más consultas en el periodo: " + consultaInfo.getMedico().getNombres() + consultaInfo.getMedico().getApellidos()
                    + "\n La cantidad de consultas total es de: "+consultaInfo.getCantidad());
                    limpiarCampos();
            }else {
            JOptionPane.showMessageDialog(null, "No hay consultas registradas en el periodo indicado.");
            }
        } 
    }//GEN-LAST:event_btnMedicosConsultasActionPerformed

