    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        if(filtrico.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Seleccione un dia libre");
            return;
        }
        
        Date x = filtrico.getDate();
        LocalDate fecha = new  java.sql.Date(x.getTime()).toLocalDate();
        try{
            controladorDescanso.guardarDiaLibre(fecha, medico);
            actualizarTabla();
            bloquear(); 
        }catch(DiaConCitaException ex){
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }  
    }//GEN-LAST:event_btnSelecionarActionPerformed

