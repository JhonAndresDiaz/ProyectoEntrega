    private void btnCancelarDiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarDiaActionPerformed
        controladorDescanso.eliminar(medico);
        bloquear();
        actualizarTabla();
        btnSelecionar.setEnabled(true);      
    }//GEN-LAST:event_btnCancelarDiaActionPerformed

    public void bloquear(){
        ArrayList<DiaDescanso> lista = medico.getDiaDeDescanso();
        ArrayList<DiaDescanso> nueva = new ArrayList<>();
                
        for (int i = 0; i < lista.size(); i++) {
            if(lista.get(i).getEstadoDia().equals("Activo"));
                nueva.add(lista.get(i));
        }
        if(nueva.size() >= 1) {
            btnSelecionar.setEnabled(false);      
        }      
    }
    
    private void actualizarTabla(){
        ArrayList<DiaDescanso> diasLibre = controladorDescanso.obtenerDiaLibre(medico);
        try{
            for (int i = 0; i < 3 ; i++) {
                for (int j = 0; j < modelo.getRowCount(); j++) {
                    modelo.removeRow(j);
                }
            }
        }catch(NullPointerException e){
        }
            try{
                for (int i = 0; i < diasLibre.size() ; i++) {
                    DiaDescanso aux = diasLibre.get(i);                            
                    Object[] ob = {aux.getDiaSeleccionado().toString() , aux.getEstadoDia()};
                    modelo.addRow(ob);                     
                }
            }catch(NullPointerException e){        
            }
    }
    
